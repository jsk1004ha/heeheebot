function startLocalLavalink({ jarPath, runtimeDir, spawnFn, logger }) {
  const logPath = join(runtimeDir, 'lavalink.log');
  const logFd = openSync(logPath, 'a');
  const child = spawnFn('java', ['-jar', jarPath], {
    cwd: runtimeDir,
    stdio: ['ignore', logFd, logFd],
    windowsHide: true
  });
  closeSync(logFd);
  child.on?.('error', (error) => logger.error?.('로컬 Lavalink 시작 실패:', error));
  child.on?.('exit', (code, signal) => {
    if (code !== 0 && signal !== 'SIGTERM') {
      logger.warn?.(`로컬 Lavalink가 종료되었습니다. code=${code} signal=${signal}. 로그: ${logPath}`);
    }
  });
  logger.log?.(`로컬 Lavalink를 시작했습니다. 로그: ${logPath}`);
  return child;
}

async function waitForLavalinkReady(config, { fetchFn, logger }) {
  const deadline = Date.now() + config.lavalink.readyTimeoutMs;
  let lastError = null;

  while (Date.now() < deadline) {
    try {
      if (await isLavalinkReachable(config, fetchFn)) {
        logger.log?.('로컬 Lavalink 준비 완료.');
        return true;
      }
    } catch (error) {
      lastError = error;
    }
    await delay(DEFAULT_READY_POLL_MS);
  }

  throw new Error(`Lavalink가 ${config.lavalink.readyTimeoutMs}ms 안에 준비되지 않았습니다. data/music-runtime/lavalink.log를 확인해주세요.${lastError ? ` 마지막 오류: ${lastError.message}` : ''}`);
}

async function isLavalinkReachable(config, fetchFn) {
  try {
    const protocol = config.lavalink.secure ? 'https' : 'http';
    const response = await fetchFn(`${protocol}://${config.lavalink.host}:${config.lavalink.port}${LAVALINK_VERSION_ENDPOINT}`, {
      signal: AbortSignal.timeout(2_000)
    });
    return response.ok;
  } catch {
    return false;
  }
}

async function ensureYtDlpBinary({ config, runtimeDir, fetchFn, platform, arch, logger }) {
  const customPath = normalizeOptionalString(config.ytdlp.path);
  if (customPath && customPath !== 'yt-dlp' && existsSync(customPath)) return customPath;

  if (customPath === 'yt-dlp' && isCommandAvailable('yt-dlp')) return customPath;

  const asset = getYtDlpAsset({ platform, arch });
  const targetPath = resolve(join(runtimeDir, asset.fileName));
  if (existsSync(targetPath)) return targetPath;

  const url = config.ytdlp.assetUrl ?? asset.url;
  logger.log?.(`yt-dlp 바이너리가 없어 다운로드합니다: ${url}`);
  await downloadFile(url, targetPath, { fetchFn });
  if (platform !== 'win32') await chmod(targetPath, 0o755);
  return targetPath;
}

function getYtDlpAsset({ platform, arch }) {
  const base = 'https://github.com/yt-dlp/yt-dlp/releases/latest/download';
  if (platform === 'win32') {
    return { fileName: `yt-dlp${WINDOWS_EXECUTABLE_SUFFIX}`, url: `${base}/yt-dlp.exe` };
  }
  if (platform === 'darwin') {
    return { fileName: 'yt-dlp_macos', url: `${base}/yt-dlp_macos` };
  }
  if (platform === 'linux' && arch === 'arm64') {
    return { fileName: 'yt-dlp_linux_aarch64', url: `${base}/yt-dlp_linux_aarch64` };
  }
  if (platform === 'linux') {
    return { fileName: 'yt-dlp_linux', url: `${base}/yt-dlp_linux` };
  }
  return { fileName: 'yt-dlp', url: `${base}/yt-dlp` };
}

async function downloadFile(url, destination, { fetchFn }) {
  const response = await fetchFn(url, {
    headers: { 'User-Agent': 'heeheebot-music-setup' }
  });
  if (!response.ok) throw new Error(`다운로드 실패 (${response.status}): ${url}`);

  mkdirSync(dirname(destination), { recursive: true });
  const tempPath = `${destination}.tmp-${process.pid}-${Date.now()}`;
  await pipeline(Readable.fromWeb(response.body), createWriteStream(tempPath));
  await rename(tempPath, destination);
}

function assertJava17Available({ spawnSyncFn }) {
  const result = spawnSyncFn('java', ['-version'], { encoding: 'utf8' });
  if (result.error || result.status !== 0) {
    throw new Error('Lavalink 자동 실행에는 Java 17 이상이 필요합니다. Java를 설치한 뒤 다시 npm start를 실행해주세요.');
  }

  const output = `${result.stderr ?? ''}\n${result.stdout ?? ''}`;
  const major = parseJavaMajorVersion(output);
  if (major !== null && major < 17) {
    throw new Error(`Lavalink 자동 실행에는 Java 17 이상이 필요합니다. 현재 Java ${major}로 감지되었습니다.`);
  }
}

function parseJavaMajorVersion(output) {
  const match = String(output).match(/version "(\d+)(?:\.(\d+))?/i);
  if (!match) return null;
  const first = Number(match[1]);
  const second = Number(match[2]);
  if (first === 1 && Number.isFinite(second)) return second;
  return Number.isFinite(first) ? first : null;
}

function stopLocalProcess(child, logger) {
  if (!child || child.killed) return;
  logger.log?.('로컬 Lavalink를 종료합니다.');
  child.kill('SIGTERM');
}

function isCommandAvailable(command) {
  const result = spawnSync(process.platform === 'win32' ? 'where' : 'which', [command], {
    stdio: 'ignore'
  });
  return result.status === 0;
}

function normalizeOptionalString(value) {
  if (value === undefined || value === null) return null;
  const normalized = String(value).trim();
  return normalized ? normalized : null;
}

function parseBoolean(value, defaultValue) {
  if (value === undefined || value === null || value === '') return defaultValue;
  return !['0', 'false', 'no', 'off'].includes(String(value).trim().toLowerCase());
}

function clampInteger(value, min, max) {
  const number = Number(value);
  if (!Number.isFinite(number)) return min;
  return Math.max(min, Math.min(max, Math.trunc(number)));
}

function escapeYamlString(value) {
  return String(value ?? '').replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function delay(ms) {
  return new Promise((resolveDelay) => setTimeout(resolveDelay, ms));
}
