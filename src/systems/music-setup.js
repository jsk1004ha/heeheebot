import { closeSync, createWriteStream, existsSync, mkdirSync, openSync } from 'node:fs';
import { chmod, readFile, rename, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { Readable } from 'node:stream';
import { pipeline } from 'node:stream/promises';
import { spawn, spawnSync } from 'node:child_process';
import { normalizeMusicConfig } from './music.js';

export const DEFAULT_MUSIC_RUNTIME_DIR = 'data/music-runtime';
export const DEFAULT_LAVALINK_JAR_URL = 'https://github.com/lavalink-devs/Lavalink/releases/latest/download/Lavalink.jar';
export const YOUTUBE_SOURCE_RELEASE_API = 'https://api.github.com/repos/lavalink-devs/youtube-source/releases/latest';
export const LAVALINK_VERSION_ENDPOINT = '/version';
export const GENERATED_CONFIG_MARKER = '# Generated by HeeHeeBot music auto setup';
export const FALLBACK_YOUTUBE_PLUGIN_VERSION = '1.18.1';
const DEFAULT_READY_TIMEOUT_MS = 90_000;
const DEFAULT_READY_POLL_MS = 1_500;
const WINDOWS_EXECUTABLE_SUFFIX = '.exe';

export async function ensureMusicRuntime(rawConfig = {}, {
  fetchFn = globalThis.fetch,
  spawnFn = spawn,
  spawnSyncFn = spawnSync,
  logger = console,
  platform = process.platform,
  arch = process.arch
} = {}) {
  const config = normalizeMusicAutoSetupConfig(rawConfig);
  const cleanupCallbacks = [];

  if (!config.autoSetup) {
    return {
      musicConfig: normalizeMusicConfig(config),
      cleanup() {}
    };
  }

  const runtimeDir = resolve(config.runtimeDir);
  mkdirSync(runtimeDir, { recursive: true });

  const localLavalink = !config.lavalink.host;
  if (localLavalink) {
    assertJava17Available({ spawnSyncFn });
    const jarPath = await ensureLavalinkJar({ config, runtimeDir, fetchFn, logger });
    const youtubePluginVersion = await resolveYoutubePluginVersion({ config, fetchFn, logger });
    await ensureLavalinkApplicationConfig({ config, runtimeDir, youtubePluginVersion, logger });

    config.lavalink.host = '127.0.0.1';
    config.lavalink.secure = false;

    if (config.lavalink.autoStart && !(await isLavalinkReachable(config, fetchFn))) {
      const child = startLocalLavalink({ jarPath, runtimeDir, spawnFn, logger });
      cleanupCallbacks.push(() => stopLocalProcess(child, logger));
      await waitForLavalinkReady(config, { fetchFn, logger });
    }
  }

  if (config.ytdlp.enabled && config.ytdlp.autoDownload) {
    config.ytdlp.path = await ensureYtDlpBinary({ config, runtimeDir, fetchFn, platform, arch, logger });
  }

  return {
    musicConfig: normalizeMusicConfig(config),
    cleanup() {
      for (const callback of cleanupCallbacks.reverse()) callback();
    }
  };
}

export function normalizeMusicAutoSetupConfig(config = {}) {
  const normalized = normalizeMusicConfig(config);
  const autoSetup = parseBoolean(config.autoSetup, true);
  const runtimeDir = normalizeOptionalString(config.runtimeDir) ?? DEFAULT_MUSIC_RUNTIME_DIR;

  return {
    ...normalized,
    autoSetup,
    runtimeDir,
    lavalink: {
      ...normalized.lavalink,
      autoStart: parseBoolean(config.lavalink?.autoStart ?? config.autoStartLavalink, true),
      jarPath: normalizeOptionalString(config.lavalink?.jarPath),
      jarUrl: normalizeOptionalString(config.lavalink?.jarUrl) ?? DEFAULT_LAVALINK_JAR_URL,
      configPath: normalizeOptionalString(config.lavalink?.configPath),
      overwriteGeneratedConfig: parseBoolean(config.lavalink?.overwriteGeneratedConfig, true),
      youtubePluginVersion: normalizeOptionalString(config.lavalink?.youtubePluginVersion),
      readyTimeoutMs: clampInteger(config.lavalink?.readyTimeoutMs ?? DEFAULT_READY_TIMEOUT_MS, 5_000, 300_000)
    },
    ytdlp: {
      ...normalized.ytdlp,
      enabled: parseBoolean(config.ytdlp?.enabled, autoSetup),
      autoDownload: parseBoolean(config.ytdlp?.autoDownload, true),
      assetUrl: normalizeOptionalString(config.ytdlp?.assetUrl)
    }
  };
}

async function ensureLavalinkJar({ config, runtimeDir, fetchFn, logger }) {
  const jarPath = resolve(config.lavalink.jarPath ?? join(runtimeDir, 'Lavalink.jar'));
  if (existsSync(jarPath)) return jarPath;

  logger.log?.(`Lavalink.jar가 없어 다운로드합니다: ${config.lavalink.jarUrl}`);
  await downloadFile(config.lavalink.jarUrl, jarPath, { fetchFn });
  return jarPath;
}

async function resolveYoutubePluginVersion({ config, fetchFn, logger }) {
  if (config.lavalink.youtubePluginVersion) return config.lavalink.youtubePluginVersion;

  try {
    const response = await fetchFn(YOUTUBE_SOURCE_RELEASE_API, {
      headers: { 'User-Agent': 'heeheebot-music-setup' }
    });
    if (!response.ok) throw new Error(`GitHub release API ${response.status}`);
    const payload = await response.json();
    const tag = normalizeOptionalString(payload?.tag_name);
    if (tag) return tag.replace(/^v/i, '');
  } catch (error) {
    logger.warn?.(`YouTube source 최신 버전을 확인하지 못해 fallback ${FALLBACK_YOUTUBE_PLUGIN_VERSION}을 사용합니다:`, error);
  }

  return FALLBACK_YOUTUBE_PLUGIN_VERSION;
}

async function ensureLavalinkApplicationConfig({ config, runtimeDir, youtubePluginVersion, logger }) {
  const configPath = resolve(config.lavalink.configPath ?? join(runtimeDir, 'application.yml'));
  if (existsSync(configPath)) {
    const existing = await readFile(configPath, 'utf8');
    if (!existing.includes(GENERATED_CONFIG_MARKER) || !config.lavalink.overwriteGeneratedConfig) {
      logger.log?.(`기존 Lavalink application.yml을 유지합니다: ${configPath}`);
      return configPath;
    }
  }

  const yaml = createLavalinkApplicationYaml({
    port: config.lavalink.port,
    password: config.lavalink.password,
    youtubePluginVersion
  });
  mkdirSync(dirname(configPath), { recursive: true });
  await writeFile(configPath, yaml, 'utf8');
  logger.log?.(`Lavalink application.yml을 준비했습니다: ${configPath}`);
  return configPath;
}

